         v(3)=v(3)+az(i)*wx
         wx=wx*x
      enddo
*
*     Compute the positions (periodic and Poisson terms)
*     --------------------------------------------------
*
      imax=0
      wx=1.d0
      do m=0,2
         do iv=1,3
            w(iv)=0.d0
         enddo
         imin=imax+1
         imax=imax+nf(m)
         do i=imin,imax
            f=fq(i)*fx
            cf=cos(f)
            sf=sin(f)
            w(1)=w(1)+cx(i)*cf+sx(i)*sf
            w(2)=w(2)+cy(i)*cf+sy(i)*sf
            w(3)=w(3)+cz(i)*cf+sz(i)*sf
         enddo
         do iv=1,3
            v(iv)=v(iv)+w(iv)*wx
         enddo
         wx=wx*x
      enddo
*
*     Compute the velocities (secular terms)
*     --------------------------------------
*
      wt=2.d0/dt
      v(4)=0.d0
      v(5)=0.d0
      v(6)=0.d0
      wx=1.d0
      do i=1,3
         v(4)=v(4)+i*ax(i)*wx
         v(5)=v(5)+i*ay(i)*wx
         v(6)=v(6)+i*az(i)*wx
         wx=wx*x
      enddo
      v(4)=wt*v(4)
      v(5)=wt*v(5)
      v(6)=wt*v(6)
*
*     Compute the velocities (periodic and Poisson terms)
*     ---------------------------------------------------
*
      imax=0
      wx=1
      do m=0,2
         imin=imax+1
         imax=imax+nf(m)
         do i=imin,imax
            fw=fq(i)
            f=fw*fx
            cf=cos(f)
            sf=sin(f)
            v(4)=v(4)+fw*(sx(i)*cf-cx(i)*sf)*wx
            v(5)=v(5)+fw*(sy(i)*cf-cy(i)*sf)*wx
            v(6)=v(6)+fw*(sz(i)*cf-cz(i)*sf)*wx
            if (m.gt.0) then
               v(4)=v(4)+m*wt*(cx(i)*cf+sx(i)*sf)*wy
               v(5)=v(5)+m*wt*(cy(i)*cf+sy(i)*sf)*wy
               v(6)=v(6)+m*wt*(cz(i)*cf+sz(i)*sf)*wy
            endif
         enddo
         wy=wx
         wx=wx*x
      enddo
*
*     Stock the results
*     -----------------
*
      do iv=1,6
         r(iv)=v(iv)/1.d10
      enddo
*
*     Exit
*     ----
*
      return
      end
