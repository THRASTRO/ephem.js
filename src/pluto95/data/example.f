      program EXAMPLE
c
c     --------------------------------------------------------------
c     This is just an example for computing rectangular position and
c     velocities of Pluto for 36 dates by step of -30 days :
c     from  January   1st 2000 12h,  Julian Date 2451545.0
c     to    February 15th 1997 12h,  Julian Date 2450495.0.
c     --------------------------------------------------------------
c
      implicit double precision (a-h,o-z)
      dimension r(6)
c
      t0 = 2451545.d0
      ndat = 36
      step = -30
c
      do i=1,ndat
         tjd = t0 + (i-1)*step
         call PLUTO (tjd,r,ierr)
         write (*,1000)  tjd, (r(k),k=1,6)
      enddo
c
 1000 format(/'    Rectangular Coordinates of Pluto at Date: JD',F16.8
     .       /'  X=',F18.14,'  Y=',F18.14,'  Z=',F18.14,' au'
     .       /'  X.=',F17.14,'  Y.=',F17.14,'  Z.=',F17.14,' au/day')
c
      stop
      end
