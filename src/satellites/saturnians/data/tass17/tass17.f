      SUBROUTINE POSIRED(DJ,IS,XYZ,VXYZ)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION XYZ(3),VXYZ(3),ELEM(6),DLO(8)
      if(IS.EQ.7) then
       CALL ELEMHYP(DJ,ELEM)
      ELSE
       CALL CALCLON(DJ,DLO)
       CALL CALCELEM(DJ,IS,ELEM,DLO)
      END IF
      CALL EDERED(ELEM,XYZ,VXYZ,IS)
      RETURN
      END
